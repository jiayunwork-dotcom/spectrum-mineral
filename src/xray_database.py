_xray_db = None


def get_xray_database() -> List[XRayLine]:
    """获取X射线特征线数据库"""
    global _xray_db
    if _xray_db is None:
        _xray_db = [
            XRayLine(
                element=elem,
                atomic_number=z,
                line=line,
                energy=energy,
                relative_intensity=intensity,
            )
            for elem, z, line, energy, intensity in _element_xray_data
        ]
    return _xray_db


def get_elements() -> List[str]:
    """获取所有元素符号列表"""
    db = get_xray_database()
    elements = []
    seen = set()
    for line in db:
        if line.element not in seen:
            elements.append(line.element)
            seen.add(line.element)
    return elements


def get_element_lines(element: str) -> List[XRayLine]:
    """获取指定元素的所有特征线"""
    db = get_xray_database()
    return [line for line in db if line.element == element]


def find_elements_by_energy(energy: float, tolerance: float = 0.1) -> List[XRayLine]:
    """根据能量查找可能的元素特征线"""
    db = get_xray_database()
    results = []
    for line in db:
        if abs(line.energy - energy) <= tolerance:
            results.append(line)
    results.sort(key=lambda x: abs(x.energy - energy))
    return results
